package com.data;

public class ReverseString
{
	public static void main(String[] args) {
		
		String name="pramod";
		
		
		for(int i=name.length()-1;i>=0;i--) {
			System.out.print(name.charAt(i));
		}
		
		String name1="sanavi";
		
		for(int i=name1.length()-1;i>=0;i--) {
			System.out.print(name1.charAt(i));
		}
	}

}
